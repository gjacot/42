#include "../wolf3d.h"

void	ft_map_init(t_env *e)
{
	e->map[0][0] = 1;
	e->map[0][1] = 1;
	e->map[0][2] = 1;
	e->map[0][3] = 1;
	e->map[0][4] = 1;
	e->map[1][0] = 1;
	e->map[1][1] = 0;
	e->map[1][2] = 0;
	e->map[1][3] = 0;
	e->map[1][4] = 1;
	e->map[2][0] = 1;
	e->map[2][1] = 0;
	e->map[2][2] = 1;
	e->map[2][3] = 0;
	e->map[2][4] = 1;
	e->map[3][0] = 1;
	e->map[3][1] = 0;
	e->map[3][2] = 0;
	e->map[3][3] = 0;
	e->map[3][4] = 1;
	e->map[4][0] = 1;
	e->map[4][1] = 1;
	e->map[4][2] = 1;
	e->map[4][3] = 1;
	e->map[4][4] = 1;
}

void	ft_init_map(t_env *e)
{
	int i;

	i = 0;
	e->map = (int **)malloc(sizeof(int *) * 5);
	while (i != 6)
	{
		e->map[i] = (int *)malloc(sizeof(int) * 5);
		i++;
	}
	ft_map_init(e);
}
